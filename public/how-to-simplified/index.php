<h4 id="change-defaults---registry-info"><a href="#TOC">Settings - Registry Info</a></h4>
<p>Simplified AidStream will automatically register your published data on the IATI Registry. To do this it requires two pieces of information - Publisher Id and API Key.</p>
<div class="figure">
<img src="img/change.defaults-iatiregistry-no.png" title="Registry Info" /><p class="caption"></p>
</div>
<h5 id="change-defaults---registry-info---publisher-id"><a href="#TOC">Settings - Registry Info - Publisher Id</a></h5>
<p>Register into IATI registry and create a new Publisher. When you create a new Publisher in <a href="http://iatiregistry.org">IATI Registry</a>, you will have to fill up a form that includes <strong>publisher id</strong> - which is also required in AidStream to register automatically in the IATI Registry. You may also choose to do the register manually in the IATI Registry.</p>
<h5 id="change-defaults---registry-info---api-key"><a href="#TOC">Settings - Registry Info - API Key</a></h5>
<p>If you click on <strong>My account</strong> in IATI Registry, you should see <strong>API Key</strong> for use in AidStream.</p>
<p>By default, <strong>Automatically update the IATI Registry when publishing files</strong> is set to No. It means that when you publish files in Simplified AidStream, IATI XML files will be created but you will have to click <strong>Register with IATI Registry</strong> under <strong>List Published Files</strong> to register your XML files with IATI Registry. This option helps to create publish XML files without having to create account in IATI Registry.</p>
<p>By default, <strong>Automatically update the IATI Registry when publishing files</strong> is set to <strong>No</strong>. It means that when you publish files in AidStream, IATI XML files will be created but you will have to click <strong>Register with IATI Registry</strong> under <strong>List Published Files</strong> to register your XML files with IATI Registry. This option helps to create publish XML files without having to create account in IATI Registry.</p>
