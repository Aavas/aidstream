<h4 id="change-defaults---reporting-organisation"><a href="#TOC">Settings - Reporting Organisation</a></h4>
<p>The <strong>Reporting Organisation</strong> is a mandatory field which clearly identifies the organisation that is publishing the data. All activities recorded in an AidStream account must contain the same Reporting Organisation. The Reporting Organisation may report data on activities funded and/or implemented by different organisations.</p>
<p>As per IATI Standard, the following information is needed for the Reporting Organisation field.</p>
<ul>
<li>Reporting Organisation Identifier</li>
<li>Reporting Organisation Type</li>
<li>Reporting Organisation Name</li>
<li>Reporting Organisation Language - Optional</li>
</ul>
<div class="figure">
<img src="img/change.defaults-reporting.organisation.png" title="Reporting Organisation" /><p class="caption"></p>
</div>
<h5 id="change-defaults---reporting-organisation-identifier"><a href="#TOC">Settings - Reporting Organisation Identifier</a></h5>
<p>IATI requires each organisation to be referenced by a globally unique identifier. Some organisations may already have a code assigned to them in CRS. These codes can be found <a href="http://iatistandard.org/codelists/organisation">Organisation Codelist</a></p>
<p>However, many organisations will not have an existing code and one will need to be generated by the organisation for the purpose of IATI. This is done using a three-part key consisting of the followings</p>
<ul>
<li>Country
<ul>
<li>The country is the 3166-1 alpha-2 code for the country in which the registration agency is based. See <a href="http://iatistandard.org/codelists/country">Country Codelist</a> for the codes.</li>
</ul></li>
<li>Registration agency
<ul>
<li>The registration agency is a code for a recognised agency that registers organisations. Eg. in the UK,there are three registration agencies: Companies House, the Charities Commissioner, and Revenue &amp; Customs. Use their initials.</li>
</ul></li>
<li>Registration number
<ul>
<li>The registration number is the identifier issued by the relevant agency. Eg Oxfam-GB's identifier is GB-CHC-202918-LACA34 where GB-CHC refers to the UK Charity Commissioners)</li>
</ul></li>
</ul>
