<h3 id="simplified-aidstream-activity-states"><a href="#TOC">Simplified Aidstream Activity States</a></h3>
<p>Simplified AidStream maintains a simple workflow to ensure that the activities are properly verified before it gets registered in IATI Registry for public view. The following states are maintained in the activities. - Edit - Completed - Verified - Published When the activity is in any one of the states, any further changes in the activity will return its state back to <strong>Editing</strong>. The entire flow has to be followed before the changes gets updated in the activity xml files and registered in IATI Registry. It is recommended that different users are given access to each of these roles for the workflow.</p>
